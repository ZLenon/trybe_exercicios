# Exercício 1:
# No terminal, inicialize duas variáveis a e b, sendo a = 10 e b = 5.
# Mostre o resultado das 7 operações básicas (soma, subtração, multiplicação,
# divisão, divisão inteira, potenciação e módulo) envolvendo essas variáveis.

a = 10
b = 5

print("Soma {}".format(a + b))
print("Subtração {}".format(a - b))
print("Mutiplicação {}".format(a * b))
print("Divisão {}".format(a / b))
print("Modulo {}".format(a % b))
print("Divisão inteira {}".format(a // b))
print("Potencialização {}".format(a**b))
